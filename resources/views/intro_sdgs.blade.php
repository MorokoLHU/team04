function handleMouseMove(e){
  document.addEventListener('mousemove', (e) => {
    if (e.clientX > window.innerWidth * 0.9) {
      scrollButtons.forEach(btn => btn.style.opacity = '1');  // 顯示所有按鈕
     }
    else {
      scrollButtons.forEach(btn => btn.style.opacity = '0');  // 隱藏所有按鈕
    
    }
  });
}
document.addEventListener('DOMContentLoaded', () => {
  document.addEventListener('mousemove', handleMouseMove);
});

    // 點擊按鈕時滾動到特定 section
const scrollButtons = document.querySelectorAll('.scroll-button');
    scrollButtons.forEach((btn, index) => {
      btn.addEventListener('click', () => {
         // 移除滑鼠移動事件
         document.removeEventListener('mousemove', handleMouseMove);
        // 隱藏所有按鈕
        scrollButtons.forEach(button => {
          button.style.opacity = '0';  // 隱藏按鈕
          button.style.pointerEvents = 'none';  // 防止點擊
            });
        // 移除滑鼠移動事件


        sections[index].scrollIntoView({ behavior: 'smooth' }); // 根據索引滾動到相應的 section

    // 在一段時間後顯示按鈕
    setTimeout(() => {
      scrollButtons.forEach(button => {
        
        button.style.pointerEvents = 'auto';  // 恢復點擊
        document.addEventListener('mousemove', handleMouseMove);
      });
      
    }, 800);  // 0.75秒後重新顯示按鈕，時間可調整
    
      });
    });

   
    const sections = document.querySelectorAll('.section');
    const observer = new IntersectionObserver(entries => {
      entries.forEach(entry => {
        if (entry.target.classList.contains('no-fade')) {
          return; // 如果包含 no-fade 類別，則跳過
        }

        if (entry.isIntersecting) {
          entry.target.style.opacity = 1;
        } else {
          entry.target.style.opacity = 0;
        }
      });
    }, { threshold: 0.5 });
    sections.forEach(section => {
        const sectionTop = section.offsetTop;
        const sectionBottom = sectionTop + section.offsetHeight;
      observer.observe(section);
    });

sections.forEach(section => {
